// User.js
export default class User {
  constructor(firstName, lastName, email, password, country, city, dob, gender) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.email = email;
    this.password = password;
    this.country = country;
    this.city = city;
    this.dob = dob;
    this.gender = gender;
  }
}
